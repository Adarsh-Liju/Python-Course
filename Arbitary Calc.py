import math as m #importing math 
ro_value=round(abs(-4.2))#assigning values to round the value of -4.3 and then takes the absolute value of that result.
ceil_value=m.ceil(m.sin(34.5))#assigning values that takes the ceiling of sine of 34.5.
floor_value=m.floor(-2.8)#assigning values to produces the floor of -2.8.
print("The Absolute value is : ",ro_value)#printing their values
print("The Ceiling value is : ",ceil_value)
print("The FLoor value is : ",floor_value)